package com.javafun;

public class While_loop {

	public static void main(String[] args) {
		int seat=10;
		
		while(seat>0) {
			System.out.println("Current seat Available : "+seat);
			seat--;			
		}
		System.out.println("Seats are Filled");
		

	}

}
